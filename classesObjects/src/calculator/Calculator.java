package calculator;

public class Calculator {
    public double number1;
    public double number2;

    public double addition (){
        return number1 + number2;
    }

    public double subtraction (){
        return number1 - number2;
    }

    public double multiplication (){
        return number1 * number2;
    }

    public double division (){
        return number1 / number2;
    }
}
